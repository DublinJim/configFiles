public class Body {
    public int torso;
    public int rArm;
    public int lArm;
    public int lLeg;
    public int head;

    public Body() {
    }

    @Override
    public String toString() {
        return "Body{" +
                "torso=" + torso +
                ", rArm=" + rArm +
                ", lArm=" + lArm +
                ", lLeg=" + lLeg +
                ", head=" + head +
                '}';
    }
}

