public class Body {
    public String rArm;
    public String lArm;
    public String rLeg;
    public String lLeg;
    public String head;
    public String torso;

    public Body(String rArm, String lArm, String rLeg, String lLeg, String head, String torso) {
        this.rArm = rArm;
        this.lArm = lArm;
        this.rLeg = rLeg;
        this.lLeg = lLeg;
        this.head = head;
        this.torso = torso;
    }
}
